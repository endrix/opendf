package net.sf.opendf.hades.des.schedule;

/**
 * PostfireHandler objects can be registered during the firing of an event with a scheduler.
 * They will be executed at the end of the firing. The intention is that postfire handlers 
 * are used for administrative work that needs to be done between firings, when the system 
 * is in a quiescent state, such as producing animation events. Postfire handlers should
 * not modify the simulation state.
 * 
 * @author jornj
 */

public interface PostfireHandler {
	
	void postfire();
}
